public class Number3 {
    public static void main(String[] args) {
        System.out.println(Math.round(2.7)); // 3
        System.out.println(Math.round(6.2)); //6
        System.out.println(Math.floor(8.7)); //8
        System.out.println(Math.ceil(5.2)); //6
        System.out.println(Math.random());// выводит рандомное число >=0 и <1
        System.out.println(Math.abs(-3));// считает модуль числа
        System.out.println(Math.max(33,44));// находит максимальное число
        System.out.println(Math.min(3,88));// находит минимальное число
    }
}
